# Программа для вычисления среднего арифметического чисел из списка
while True:
    try:
        # Запрос данных у пользователя
        user_input = input("Введите числа через пробел: ")

        # Разделение строки на список и преобразование в числа
        numbers = user_input.split()
        if not numbers:
            raise ValueError("Вы не ввели ни одного числа. Попробуйте снова.")
        numbers = [float(num) for num in numbers]

        # Вычисление среднего арифметического
        average = sum(numbers) / len(numbers)
        print(f"Среднее арифметическое: {average}")
        break  # Завершение программы при успешном выполнении

    except ValueError as e:
        # Обработка ошибок преобразования в числа или пустого ввода
        print(f"Ошибка: {e}. Попробуйте снова.")

    except ZeroDivisionError:
        # Обработка случая, когда список пуст
        print("Ошибка: Невозможно вычислить среднее арифметическое для пустого списка. Попробуйте снова.")